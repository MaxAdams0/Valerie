// ================================================================================================
// Format: NR[n]_REG
// Ch1: n= 10-14 (5)
// Ch2: n= 21-24 (4)
// Ch3: n= 30-34 (5)
// Ch4: n= 41-44 (4)

void snd_init(void) {
	NR52_REG = 0x80; // 1000 0000, turns on sound
	NR51_REG = 0xFF; // 1111 1111, selects (all) channels
	NR50_REG = 0x77; // 0111 0111, sets max vol for L&R (what bits do?)
}

void snd_test_sound(void) {
	NR10_REG = 0X00;
    NR11_REG = 0X81;
    NR12_REG = 0X43;
    NR13_REG = 0X73;
    NR14_REG = 0X86;
}

